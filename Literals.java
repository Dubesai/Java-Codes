import java.lang.*;

class Literals
{
    public static void main(String args[])
    {
        byte a=10;
        byte b=0b1010;
        byte c=0X12;
        byte d=0XA;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}