import java.lang.*;

class Variables
{
    public static void main(String args[])
    {
        int a=300;
        short b=100;
        float c=39.3f;
        byte d=123;
        String e="Hello";
        char f='A';

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    
    }
}